import type {
  AdminOrder,
  AdminOrderStatus,
  AdminOrderTimelineEvent,
} from "@/types/admin-order";

const GEM = "/images/showcase/gemstone.png";

function timeline(
  status: AdminOrderStatus,
  createdAt: string,
): AdminOrderTimelineEvent[] {
  const base = new Date(createdAt).getTime();
  const events: AdminOrderTimelineEvent[] = [
    {
      id: "tl_placed",
      status: "pending",
      label: "Order Placed",
      description: "Concierge order received and awaiting review.",
      timestamp: new Date(base).toISOString(),
    },
  ];

  if (["processing", "shipped", "delivered"].includes(status)) {
    events.push({
      id: "tl_processing",
      status: "processing",
      label: "Processing",
      description: "Gemologist verification and packaging in progress.",
      timestamp: new Date(base + 2 * 60 * 60 * 1000).toISOString(),
    });
  }

  if (["shipped", "delivered"].includes(status)) {
    events.push({
      id: "tl_shipped",
      status: "shipped",
      label: "Shipped",
      description: "Insured courier dispatched with white-glove delivery.",
      timestamp: new Date(base + 24 * 60 * 60 * 1000).toISOString(),
    });
  }

  if (status === "delivered") {
    events.push({
      id: "tl_delivered",
      status: "delivered",
      label: "Delivered",
      description: "Signed delivery confirmed by concierge recipient.",
      timestamp: new Date(base + 72 * 60 * 60 * 1000).toISOString(),
    });
  }

  if (status === "cancelled") {
    events.push({
      id: "tl_cancelled",
      status: "cancelled",
      label: "Cancelled",
      description: "Order cancelled per customer request.",
      timestamp: new Date(base + 4 * 60 * 60 * 1000).toISOString(),
    });
  }

  return events;
}

function address(
  name: string,
  city: string,
  state: string,
  postal: string,
): AdminOrder["shipping"] {
  return {
    name,
    line1: "42 Maharaja Heritage Lane",
    line2: "Penthouse Suite",
    city,
    state,
    postalCode: postal,
    country: "India",
    phone: "+91 98765 43210",
  };
}

